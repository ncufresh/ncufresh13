		<h1>郵局</h1><br />
	<p>
		中大郵局內一共有兩台ATM自動櫃員機（新研舍1F也提供一台ATM自動櫃員機），其中一台提供百元鈔服務，大家可以多加利用。除了ATM外，另外還有一台補摺機可供使用。如需櫃檯服務，則需先抽取號碼牌等候叫號。除了櫃檯服務外，其他都是24小時開放。另外在寒暑假前夕，郵局也會提供包裹寄送的服務，讓同學可以把自己的行李裝箱寄回家。

		營業時間：周一至周五 8:00 - 17:00<br />

		校內分機：57395<br />

		連絡電話：(03)490-2528<br />
